package com.study.ch06;

public class For01 {
    public static void main(String[] args) {

        //일정한 개수 반복횟수

        for(int i =0; i < 10; i++) {
            System.out.println("i: " + i);
        }

        //조건

        int i = 0;
        while (i < 10) {
            System.out.println("i: " + i);
            i++;
        }
    }
}
